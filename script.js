function renderTags() {
    const tagsGrid = document.querySelector('.tags-grid');
    
    discordTags.forEach(tag => {
        const tagCard = document.createElement('a');
        tagCard.href = tag.discordUrl;
        tagCard.target = '_blank';
        tagCard.rel = 'noopener noreferrer';
        tagCard.className = 'tag-card';
        
        tagCard.innerHTML = `
            <div class="tag-content">
                <div class="tag-icon-container">
                    <img src="${tag.iconUrl}" alt="${tag.name} icon" class="tag-icon">
                    <div class="tag-code-badge">
                        ${tag.code.length > 3 ? tag.code.substring(0, 1) : tag.code}
                    </div>
                </div>
                <div class="tag-info">
                    <div class="tag-header">
                        <h3 class="tag-name">${tag.code}</h3>
                                               <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24"
     fill="none" viewBox="0 0 24 24" stroke="#ffffff" stroke-width="2"
     class="tag-link-icon">
  <path d="M18 13v6a2 2 0 0 1-2 2H6a2 2 0 0 1-2-2V8a2 2 0 0 1 2-2h6" />
  <polyline points="15 3 21 3 21 9" />
  <line x1="10" y1="14" x2="21" y2="3" />
</svg>
                    </div>
                    <p class="tag-description">${tag.name}</p>
                </div>
            </div>
        `;
        
        tagsGrid.appendChild(tagCard);
    });
}


function openModal() {
    document.getElementById('imageModal').classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    document.getElementById('imageModal').classList.remove('active');
    document.body.style.overflow = 'auto';
}


function showNotice() {
    const notice = document.getElementById('serverNotice');
    setTimeout(() => {
        notice.classList.add('active');
    }, 2000);
}

function closeNotice() {
    const notice = document.getElementById('serverNotice');
    notice.classList.remove('active');
}


document.getElementById('imageModal').addEventListener('click', (e) => {
    if (e.target === document.getElementById('imageModal')) {
        closeModal();
    }
});


document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') {
        closeModal();
    }
});


document.addEventListener('DOMContentLoaded', () => {
    renderTags();
    showNotice();
});